/**
 * 小结：
 * 在 OOP中，class和 instance是"模板"和"实例"的关系
 * 定义 class就是定义了一种数据类型，对应的 instance是这种数据类型的实例
 * class定义的 field，在每个 instance都会拥有各自的 filed，且互不干扰
 * 通过 new操作符创建新的 instance，然后用变量指向它，即可通过变量来引用这个 instance
 * 访问实例字段的方法是 变量名.字段名
 * 指向 instance的变量都是引用变量
 */
